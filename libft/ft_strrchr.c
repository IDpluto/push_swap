#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*s1;

	s1 = NULL;
	while (1)
	{
		if (*(char *)s == (char)c)
			s1 = (char *)s;
		if (*s++ == '\0')
			return (s1);
	}
}
